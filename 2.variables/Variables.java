
public class Variables {
	
	public static void main(String args[]){	
	
		int x = 5;
			System.out.println("x= " + x);
		int y = 7;
			System.out.println("y= " + y);
			
		float  b = 10.5f;
			System.out.println("b= " + b);
			
		double sum = x+y+b;
		System.out.println("Total= " + sum);
		
		boolean t = true;
			System.out.println("This is an Boolean Variable = " + t);
	}
	
}



